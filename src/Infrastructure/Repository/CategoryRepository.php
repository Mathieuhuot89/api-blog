namespace App\Infrastructure\Repository;

use App\Domain\Entity\Category as EntityCategory;
use App\Domain\Repository\CategoryInterface;
use App\Infrastructure\Entity\Category;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Category>
 *
 * @method Category|null find($id, $lockMode = null, $lockVersion = null)
 * @method Category|null findOneBy(array $criteria, array $orderBy = null)
 * @method Category[]    findAll()
 * @method Category[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class CategoryRepository extends ServiceEntityRepository implements CategoryInterface
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Category::class);
    }

    public function save(Category $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Category $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function get(int $id): ?EntityCategory
    {
         $categoryDoctrine = $this->find($id);
         if ($categoryDoctrine === null) {
            return null;
         }

        return new EntityCategory($categoryDoctrine->getId(), $categoryDoctrine->getName());
    } 

    /**
     * @return EntityCategory[]
     */
    public function getAll(): array
    {
        $categoriesDoctrine = $this->findAll();
        $categoriesDomain = [];
        
        foreach ($categoriesDoctrine as $categoryDoctrine) {
            $categoriesDomain[] = new EntityCategory($categoryDoctrine->getId(), $categoryDoctrine->getName());
        }
        
        return $categoriesDomain;
    }

